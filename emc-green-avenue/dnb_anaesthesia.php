<p><strong>WHAT DNB IS?</strong><br /> National Board of Examinations in Medical Sciences (NBEMS) was established in 1975 by the Government of India with the prime objective of improving the quality of Medical Education by establishing high and uniform standards of postgraduate examinations in modern medicine on All India basis. NBEMS conducts post graduate and post-doctoral examinations in approved disciplines leading to the award of Diplomate of National Board (DNB), Doctorate of National Board (DrNB) and Fellow of National Board (FNB). The setting up of a National Body to conduct post graduate medical examination and training has provided common standards and mechanism of evaluation of minimum level of desired knowledge and competencies and fulfilment of the objectives for which postgraduate courses has been started in medical institutions.</p>
<p><strong>RECOGNITION OF DNB, DrNB &amp; FNB QUALIFICATIONS:</strong></p>
<p>&nbsp;The nomenclature of the qualification awarded by the National Board of Examinations in Medical Sciences (NBEMS) is "Diplomate of National Board&rdquo; (DNB), "Doctorate of National Board&rdquo; (DrNB) &amp; &ldquo;Fellow of National Board&rdquo; (FNB). These qualifications awarded by NBEMS in various Broad Specialties, Super specialties and Sub-Specialties are approved by the Government of India and included in the First Schedule of IMC (repealed) Act 1956 / NMC Act 2019.</p>
