<p><strong>iii. Research Ability:</strong>&nbsp;Should also acquire elementary knowledge about research methodology, including record-keeping methods, and be able to conduct a research inquiry including making a proper analysis and writing a report on its findings. Data analysis and Use of basic statistical methods require for publication.</p>
