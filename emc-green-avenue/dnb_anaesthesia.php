<p><strong>PROGRAMME GOAL</strong><br /> The main purpose is to ensure a competent working of DNB trainings in Uppal NeuroHospitals,as DNB Candidate plays a very important role in working of institutes.<br /> <strong>i. Patient care Ability:</strong>&nbsp;A postgraduate in Neurology at the end of its three year course should develop proper clinical acumen to interpret diagnostic results and correlate them with symptoms from history taking and become capable to diagnose the common clinical conditions/ disease in the specialty and to manage them effectively with success without making any serious complications and sincerely to take such accurate decision, for the patient&rsquo;s best interest including making a referral to consultation with a more experienced colleague/professional friend while dealing with any patient with a difficult condition.</p>
