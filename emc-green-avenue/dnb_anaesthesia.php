<p><strong>LEAVE RULES FOR DNB/FNB TRAINEES</strong> :</p>
<p>The following leave rules shall apply to the candidates</p>
<ol>
<li>DNB/FNB Trainees are entitled to avail leave during the course of DNB/FNB training as per the Leave Rules prescribed by NBE.</li>
<li>A DNB/FNB Trainees can avail a maximum of 30 days of leave in a year excluding regular duty off/ Gazetted holidays as per hospital/institute calendar/policy. This leave shall be processed at the institutional level.</li>
<li>Any kind of study leave is not permissible to DNB/FNB Trainees.</li>
<li>Under normal circumstances leave of one year should not be carried forward to the next year. However, in exceptional cases such as prolonged illness, the leave across the DNB/FNB training program may be clubbed together with prior approval of NBE.</li>
<li>Unauthorized absence from DNB/FNB training for more than 7 days may lead to cancellation of registration and discontinuation of the DNB/FNB training and rejoining shall not be permitted.</li>
<li>Any Leave availed by the candidate other than the eligible leave (30 days per year) shall lead to extension of DNB /FNB training. The training institute has to forward such requests to NBE along with the leave records of the candidate since his/her joining and supporting documents (if any) through the Head of the Institute with their recommendation/comments. NBE shall consider such requests on merit provided the seat is not carried over and compromise with training of existing trainees in the Department.</li>
<li>Any extension of DNB/FNB training beyond the scheduled completion date of training is permissible only under extra-ordinary circumstances with prior approval of NBE. Such extension is neither automatic nor shall be granted as a matter of routine.</li>
<li>DNB/FNB trainees are required to complete their training by a prescribed cutoff date (as per information bulletin of Exit exam) for being eligible to DNB/FNB Exit examination.</li>
<li>The eligibility for DNB/FNB Final Examination shall be determined strictly in accordance with the criteria prescribed in the respective information bulletin. DNB Trainees are entitled to avail leave during the course of DNB/FNB training as per the Leave Rules prescribed by NBE.<br /> </li>
</ol>
